#include <iostream>

using namespace std;

int main() {
    int num1 = 10;
    int num2 = 5;

    // Relational Operators
    cout << "num1 > num2 (greater than): " << (num1 > num2) << endl;
    cout << "num1 < num2 (less than): " << (num1 < num2) << endl;
    cout << "num1 >= num2 (greater than or equal to): " << (num1 >= num2) << endl;
    cout << "num1 <= num2 (less than or equal to): " << (num1 <= num2) << endl;
    cout << "num1 == num2 (equal to): " << (num1 == num2) << endl;
    cout << "num1 != num2 (not equal to): " << (num1 != num2) << endl;

    return 0;
}
